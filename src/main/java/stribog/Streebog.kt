    private var _buffer: MutableList<Byte> = mutableListOf()
    private val _epsilon: ByteArray = ByteArray(BLOCK_SIZE)
    private val _n: ByteArray = ByteArray(BLOCK_SIZE)

    open fun initialize() {
        for (i in 0 until BLOCK_SIZE) {
            _n[i] = 0x00
            _epsilon[i] = 0x00
        }
        _buffer.clear()
    }

    /**
     * Формирование хэш-значения.
     */
    protected open fun hashCore(array: ByteArray, ibStart: Int, cbSize: Int) {
        initialize()
        prepareBuffer(array, ibStart, cbSize)
        processBuffer(512)
    }

    private fun prepareBuffer(array: ByteArray, ibStart: Int, cbSize: Int) {
        val message = ByteArray(cbSize)
        System.arraycopy(array, ibStart, message, 0, cbSize)
        for (b in message) {
            _buffer.add(b)
        }
    }

    private fun processBuffer(length: Int) {
        while (_buffer.size >= BLOCK_SIZE) {
            val message = ByteArray(BLOCK_SIZE)
            for (i in 0 until BLOCK_SIZE) {
                message[i] = _buffer[i]
            }
            processBlock(message, length)
            _buffer = ArrayList(_buffer.subList(BLOCK_SIZE, _buffer.size))
        }
    }

    open fun processBlock(message: ByteArray, length: Int) {
        var length = length
        reverseBytes(message)
        compress(message, false)

        // N += |M|
        var index: Int = BLOCK_SIZE - 1
        while (length > 0) {
            require(index >= 0)
            length += UnsignedBytes.toInt(_n[index])
            _n[index] = length.toByte()
            length = length shr 8
            index--
        }

        // _epsilon += m
        var carry = 0
        index = BLOCK_SIZE - 1
        while (index >= 0) {
            carry += UnsignedBytes.toInt(_epsilon[index])
            carry += UnsignedBytes.toInt(message[index])
            _epsilon[index] = carry.toByte()
            carry = carry shr 8
            index--
        }
    }

    private fun reverseBytes(data: ByteArray) {
        val length = data.size
        if (length > 1) {
            for (i in 0 until length / 2) {
                val tmp = data[i]
                data[i] = data[length - i - 1]
                data[length - i - 1] = tmp
            }
        }
    }

    /**
     * Инициализационный вектор.
     */
    protected val _iv: ByteArray  = ByteArray(BLOCK_SIZE)

    /**
     * Функция сжатия.
     */
    private fun compress(state: ByteArray, zero: Boolean) {
        val sigma = ByteArray(BLOCK_SIZE)
        if (zero) System.arraycopy(_iv, 0, sigma, 0, 64) else for (i in 0..63) sigma[i] =
            (_iv[i] xor _n[i]).toByte()
        LPS(sigma)
        val result = ByteArray(BLOCK_SIZE)
        System.arraycopy(state, 0, result, 0, BLOCK_SIZE)
        for (round in 0..12) {
            for (i in 0 until BLOCK_SIZE) result[i] = result[i] xor sigma[i]
            if (round == 12) break
            LPS(result)

            // sigma = LPS(sigma xor _keyConsts)
            val cround: ByteArray = _keyConsts.get(round)
            for (i in 0 until BLOCK_SIZE) sigma[i] = sigma[i] xor cround[i]
            LPS(sigma)
        }
        for (i in 0 until BLOCK_SIZE) result[i] =
            (result[i] xor _iv[i] xor state[i]).toByte()
        System.arraycopy(result, 0, _iv, 0, 64)
    }

    private fun LPS(vector: ByteArray) {
        S(vector)
        P(vector)
        L(vector)
    }

    /**
     * S-преобразование. Нелинейное биективное преобразование.
     */
    private fun S(state: ByteArray) {
        for (i in 0 until BLOCK_SIZE) {
            state[i] = _sbox[UnsignedBytes.toInt(state[i])]
        }
    }

    /**
     * P - преобразование. Перестановка байт.
     */
    private fun P(state: ByteArray) {
        val prevState = ByteArray(BLOCK_SIZE)
        System.arraycopy(state, 0, prevState, 0, BLOCK_SIZE)
        for (i in 0 until BLOCK_SIZE) {
            state[i] = prevState[_tau[i].toInt()]
        }
    }

    /**
     * L - преобразование. Умножение входного вектора на бинарную матрицу.
     */
    private fun L(state: ByteArray) {
        for (i in 0..7) {
            var t = UnsignedLong.valueOf(0L)
            for (j in 0..7) {
                val stateIndex = i shl 3 or j
                val stateValue = UnsignedBytes.toInt(state[stateIndex])
                t = UnsignedLong.valueOf(
                    t.bigIntegerValue()
                        .xor(_mixMatrix[j][stateValue].bigIntegerValue())
                )
            }
            val index = i shl 3
            state[index] = t.bigIntegerValue().shiftRight(56).toByte()
            state[index + 1] = t.bigIntegerValue().shiftRight(48).toByte()
            state[index + 2] = t.bigIntegerValue().shiftRight(40).toByte()
            state[index + 3] = t.bigIntegerValue().shiftRight(32).toByte()
            state[index + 4] = t.bigIntegerValue().shiftRight(24).toByte()
            state[index + 5] = t.bigIntegerValue().shiftRight(16).toByte()
            state[index + 6] = t.bigIntegerValue().shiftRight(8).toByte()
            state[index + 7] = t.bigIntegerValue().toByte()
        }
    }

    protected var hashSize = 0

    protected open fun hashFinal(): ByteArray {
        val length: Int = addPadding()
        processBuffer(length)
        compress(_n, true)
        compress(_epsilon, true)
        val result = ByteArray(hashSize / 8)
        System.arraycopy(_iv, 0, result, 0, result.size)
        reverseBytes(result)
        return result
    }

    private fun addPadding(): Int {
        val result = _buffer.size * 8
        _buffer.add(1.toByte())
        while (_buffer.size < BLOCK_SIZE) _buffer.add(0.toByte())
        return result
    }

    /**
     * Вычисляет хэш от buffer
     */
    open fun computeHash(buffer: ByteArray): ByteArray? {
        hashCore(buffer, 0, buffer.size)
        val hashValue: ByteArray = hashFinal()
        initialize()
        return hashValue
    }

